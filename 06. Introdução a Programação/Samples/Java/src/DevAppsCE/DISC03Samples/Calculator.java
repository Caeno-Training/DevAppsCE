package DevAppsCE.DISC03Samples;

/**
 * Uma calculadora simples.
 */
public class Calculator {
    
    /**
     * Soma dois números.
     * @param a Primeiro número.
     * @param b Segundo número.
     * @return O resultado da soma dos dois números passados como argumentos.
     */
    public int Add(int a, int b) {
        return a + b;
    }
    
    /**
     * Subtrai um número de outro.
     * @param a Primeiro número.
     * @param b Segundo número, que irá subtrair do primeiro.
     * @return O resultado da subtração de b em a.
     */
    public int Subtract(int a, int b) {
        return a - b;
    }
    
}
